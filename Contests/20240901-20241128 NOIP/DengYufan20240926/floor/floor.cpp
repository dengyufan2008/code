LL n, m, ans;
int k;

LL Pow(LL x, int y = kMod - 2) {
  LL ans = 1;
  for (int i = 1; i <= y; i <<= 1) {
    if (i & y) {
      ans = ans * x % kMod;
    }
    x = x * x % kMod;
  }
  return ans;
}

int main() {
  cin.tie(0), cout.tie(0);
  ios::sync_with_stdio(0);
  cin >> n >> m >> k;
  for (int i = 0; i <= 5; i++) {
    for (int j = 0; j <= 5; j++) {
      if (i + j <= 5) {
        static int c = 0;
        ans = (ans + Pow(n, i) * Pow(m, j) % kMod * kAns[k][c++]) % kMod;
      }
    }
  }
  cout << ans << '\n';
  return 0;
}
